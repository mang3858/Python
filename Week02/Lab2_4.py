num1 = int(input("첫 번째 정수를 입력하시오: "))
num2 = int(input("두 번째 정수를 입력하시오: "))

print("%d + %d = %d" %(num1, num2, num1+ num2))
print("%d - %d = %d" %(num1, num2, num1- num2))
print("%d * %d = %d" %(num1, num2, num1* num2))
print("%d / %d = %.1f" %(num1, num2, num1/ num2))
print("%d // %d = %d" %(num1, num2, num1// num2))
print("%d %% %d = %.1f" %(num1, num2, num1 % num2)) # % 출력하려면 %% 해야 함
print("%d ** %d = %d" %(num1, num2, num1** num2))
